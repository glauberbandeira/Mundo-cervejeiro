function Becks() {
  // Remover Classe Content
  document.querySelector('#content').classList.remove('content');
  document.querySelector('#budweiser').classList.remove('budweiser');
  document.querySelector('#corona').classList.remove('corona');
  document.querySelector('#heineken').classList.remove('heineken');
  document.querySelector('#patagonia').classList.remove('patagonia');
  document.querySelector('#sol').classList.remove('sol');
  document.querySelector('#stella').classList.remove('stella');
  // Romover Id content
  document.querySelector('#content').style.display = 'none'; 
  document.querySelector('#budweiser').style.display = 'none';
  document.querySelector('#corona').style.display = 'none'; 
  document.querySelector('#heineken').style.display = 'none'; 
  document.querySelector('#patagonia').style.display = 'none'; 
  document.querySelector('#sol').style.display = 'none';

  // Adicionar Classe becks e Id becks
  document.querySelector('#becks').classList.add('becks');
  document.querySelector('#becks').style.display = 'grid';

  // Mudando a cor de fundo e cor da letra
  document.body.style.background = 'rgb(104, 126, 49)';
  document.body.style.color = '#fff'
}

